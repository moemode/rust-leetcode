    def canSumTo(i: int, j: int):
        """
        Is it possible to assign +/- to numbers in nums[i:],
        s.t. their sum is j
        """
        if i == len(nums):
            return j == 0
        if (i, j) not in memo:
            memo[(i, j)] = canSumTo(i + 1, j - nums[i]) or canSumTo(i + 1, j + nums[i])
        return memo[(i, j)]

    memo = dict()

    return canSumTo(0, 0)


